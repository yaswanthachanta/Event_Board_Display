// types.h

typedef unsigned char u8;   // u8 means "unsigned 8-bit" value (0 to 255)
typedef char s8;            // s8 means "signed 8-bit" value (-128 to 127)

typedef unsigned short u16; // u16 means "unsigned 16-bit" value (0 to 65535)
typedef short s16;          // s16 means "signed 16-bit" value (-32768 to 32767)

typedef unsigned int u32;   // u32 means "unsigned 32-bit" value (0 to ~4 billion)
typedef int s32;            // s32 means "signed 32-bit" value (~-2 billion to +2 billion)

typedef float f32;          // f32 means 32-bit floating point number
typedef double f64;         // f64 means 64-bit floating point number (more precise)
